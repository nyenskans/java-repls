package replTasksPart3;

public class Repl060 {

	public static void main(String[] args) {

		/*
		 * Write a program that uses for loop to prints multiplication of number 3 from
		 * 1 to 10
		 */
		int mult=0;
		for(int i=1; i<=10; i++) {
			mult=3*i;
			System.out.println("3*" + i+"="+mult);	
		}

	}
}