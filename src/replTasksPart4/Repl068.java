package replTasksPart4;

public class Repl068 {

	public static void main(String[] args) {

/* Nested For Loop
 		For you to do:

			Write a program to print out the pattern:

			Expected output:

			$$$$
			$  $
			$  $
			$$$$
*/		
		
		
		
		for(int i=0; i<1; i++) {
			for(int j=0; j<i; j++) {
				System.out.print("$$$$");
			}System.out.println();
		}for(int i=0; i<1; i++) {
			for(int j=0; j<i; j++) {
				System.out.print("$ $");
			}System.out.println();
		
	}

}
}